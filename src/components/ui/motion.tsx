// Animated counter
export function AnimatedCounter({
  value,
  duration = 2,
  className = "",
  suffix = "",
}: {
  value: number;
  duration?: number;
  className?: string;
  suffix?: string;
}) {
  const ref = useRef(null);
  const isInView = useInView(ref, { once: true });

  return (
    <motion.span
      ref={ref}
      initial={{ opacity: 0 }}
      animate={isInView ? { opacity: 1 } : { opacity: 0 }}
      className={className}
    >
      <motion.span
        initial={{ opacity: 0 }}
        animate={isInView ? { opacity: 1 } : { opacity: 0 }}
        transition={{ duration: 0.3 }}
      >
        {isInView && (
          <CountUp value={value} duration={duration} suffix={suffix} />
        )}
      </motion.span>
    </motion.span>
  );
}

// Count up helper component
function CountUp({
  value,
  duration,
  suffix,
}: {
  value: number;
  duration: number;
  suffix: string;
}) {
  const ref = useRef<HTMLSpanElement>(null);

  return (
    <motion.span
      ref={ref}
      initial={{ opacity: 0 }}
      animate={{ opacity: 1 }}
    >
      <motion.span
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        transition={{ duration: duration }}
      >
        {value}{suffix}
      </motion.span>
    </motion.span>
  );
}

// Floating animation for decorative elements
export function Float({
  children,
  duration = 3,
  y = 10,
  className = "",
}: {
  children: ReactNode;
  duration?: number;
  y?: number;
  className?: string;
}) {
  return (
    <motion.div
      animate={{
        y: [-y, y, -y],
      }}
      transition={{
        duration,
        repeat: Infinity,
        ease: "easeInOut",
      }}
      className={className}
    >
      {children}
    </motion.div>
  );
}

